from __future__ import annotations

import argparse
import gc
import sys
from pathlib import Path

import pandas as pd

PROJECT_ROOT = Path(__file__).resolve().parents[1]
if str(PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(PROJECT_ROOT))

from src.wm811k_utils import (
    is_labeled_failure_type,
    is_patterned_failure_type,
    normalize_nested_label,
    wafer_stats,
)


DEFAULT_INPUT = Path("LSWMD.pkl") / "LSWMD.pkl"
DEFAULT_OUT_DIR = Path("data") / "processed"


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description=(
            "Extract labeled WM-811K rows and build metadata for later EDA. "
            "This loads the original pickle once, so run it when enough RAM is free."
        )
    )
    parser.add_argument("--input", type=Path, default=DEFAULT_INPUT)
    parser.add_argument("--out-dir", type=Path, default=DEFAULT_OUT_DIR)
    parser.add_argument(
        "--metadata-only",
        action="store_true",
        help="Only write metadata CSV/summary, not subset pickle files.",
    )
    parser.add_argument(
        "--preview-rows",
        type=int,
        default=0,
        help=(
            "After loading the pickle, limit processing to the first N rows. "
            "Useful for script debugging, but it still loads the full pickle."
        ),
    )
    return parser.parse_args()


def build_metadata(df: pd.DataFrame) -> pd.DataFrame:
    required = {"waferMap", "dieSize", "lotName", "waferIndex", "trianTestLabel", "failureType"}
    missing = sorted(required.difference(df.columns))
    if missing:
        raise ValueError(f"Missing expected columns: {missing}")

    records: list[dict[str, object]] = []
    total = len(df)
    for pos, row in enumerate(df.itertuples(index=True), start=1):
        failure_type = normalize_nested_label(row.failureType)
        split = normalize_nested_label(row.trianTestLabel)
        stats = wafer_stats(row.waferMap)

        records.append(
            {
                "row_index": row.Index,
                "dieSize": row.dieSize,
                "lotName": row.lotName,
                "waferIndex": row.waferIndex,
                "trianTestLabel": split,
                "failureType": failure_type,
                "is_labeled": is_labeled_failure_type(failure_type),
                "is_patterned": is_patterned_failure_type(failure_type),
                **stats,
            }
        )

        if pos % 50000 == 0 or pos == total:
            print(f"metadata rows processed: {pos:,}/{total:,}")

    return pd.DataFrame.from_records(records)


def write_summary(metadata: pd.DataFrame, out_path: Path) -> None:
    total = len(metadata)
    labeled = int(metadata["is_labeled"].sum())
    patterned = int(metadata["is_patterned"].sum())
    unlabeled = total - labeled

    label_counts = metadata.loc[metadata["is_labeled"], "failureType"].value_counts()
    split_counts = metadata["trianTestLabel"].replace("", "(empty)").value_counts()
    shape_counts = metadata["map_shape"].value_counts().head(20)

    lines = [
        "# WM-811K Dataset Summary",
        "",
        "This report is generated by `experiments/01_extract_labeled_subset.py`.",
        "",
        "## Row Counts",
        "",
        f"- Total rows: {total:,}",
        f"- Labeled rows: {labeled:,}",
        f"- Patterned defect rows, excluding `none`: {patterned:,}",
        f"- Unlabeled rows: {unlabeled:,}",
        "",
        "## Failure Type Counts",
        "",
        "```text",
        label_counts.to_string(),
        "```",
        "",
        "## Train/Test Label Counts",
        "",
        "```text",
        split_counts.to_string(),
        "```",
        "",
        "## Top Wafer Map Shapes",
        "",
        "```text",
        shape_counts.to_string(),
        "```",
        "",
    ]
    out_path.write_text("\n".join(lines), encoding="utf-8")


def main() -> None:
    args = parse_args()
    args.out_dir.mkdir(parents=True, exist_ok=True)
    Path("reports").mkdir(exist_ok=True)

    print(f"loading pickle: {args.input}")
    df = pd.read_pickle(args.input)
    print(f"loaded DataFrame shape: {df.shape}")
    print(f"columns: {list(df.columns)}")

    if args.preview_rows:
        df = df.head(args.preview_rows).copy()
        print(f"preview mode: processing first {len(df):,} rows")

    metadata = build_metadata(df)
    metadata_dir = args.out_dir / "metadata"
    subset_dir = args.out_dir / "subsets"
    metadata_dir.mkdir(parents=True, exist_ok=True)
    subset_dir.mkdir(parents=True, exist_ok=True)

    metadata_path = metadata_dir / "labeled_metadata.csv"
    metadata.to_csv(metadata_path, index=False)
    print(f"wrote metadata: {metadata_path}")

    summary_path = Path("reports") / "dataset_summary.md"
    write_summary(metadata, summary_path)
    print(f"wrote summary: {summary_path}")

    if not args.metadata_only:
        labeled_mask = metadata["is_labeled"].to_numpy()
        patterned_mask = metadata["is_patterned"].to_numpy()

        df = df.copy()
        df["failureType_clean"] = metadata["failureType"].to_numpy()
        df["trianTestLabel_clean"] = metadata["trianTestLabel"].to_numpy()

        labeled_path = subset_dir / "labeled_subset.pkl"
        patterned_path = subset_dir / "patterned_subset.pkl"

        df.loc[labeled_mask].to_pickle(labeled_path)
        print(f"wrote labeled subset: {labeled_path}")

        df.loc[patterned_mask].to_pickle(patterned_path)
        print(f"wrote patterned subset: {patterned_path}")

    del df
    gc.collect()
    print("done")


if __name__ == "__main__":
    main()
